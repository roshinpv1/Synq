import os

_SYNQ_HOME = os.path.join(os.path.expanduser("~"), ".synq")

# Single source of truth for env-var → config-key overrides. To expose
# a new config key for environment-based override, add a row here — no
# entry-point script changes required. Coercion is driven by the type
# of the existing default, so users can keep writing plain strings in
# their .env file.
_ENV_OVERRIDES = {
    "SYNQ_LLM_PROVIDER":         "llm_provider",
    "SYNQ_DEEP_THINK_LLM":       "deep_think_llm",
    "SYNQ_QUICK_THINK_LLM":      "quick_think_llm",
    "SYNQ_LLM_BACKEND_URL":      "backend_url",
    "SYNQ_CHECKPOINT_ENABLED":   "checkpoint_enabled",
    "SYNQ_TEMPERATURE":          "temperature",
}


def _coerce(value: str, reference):
    """Coerce env-var string to the type of the existing default value."""
    if isinstance(reference, bool):
        return value.strip().lower() in ("true", "1", "yes", "on")
    if isinstance(reference, int) and not isinstance(reference, bool):
        return int(value)
    if isinstance(reference, float):
        return float(value)
    return value


def _apply_env_overrides(config: dict) -> dict:
    """Apply SYNQ_* env vars to the config dict in-place."""
    for env_var, key in _ENV_OVERRIDES.items():
        raw = os.environ.get(env_var)
        if raw is None or raw == "":
            continue
        config[key] = _coerce(raw, config.get(key))
    return config


